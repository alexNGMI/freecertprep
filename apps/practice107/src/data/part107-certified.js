function sentence(text) {
  const trimmed = String(text || '')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/[.?!]+$/, '')

  return `${trimmed}.`
}

function explain(right, wrong, takeaway) {
  return [right, wrong, takeaway].map(sentence).join(' ')
}

