function pad(value) {
  return String(value).padStart(3, '0')
}

function sentence(text) {
  const trimmed = String(text || '')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/[.?!]+$/, '')

  return `${trimmed}.`
}

function q({
  id,
  formId,
  domain,
  acsTask,
  acsCode,
  sourceRefs,
  evidenceType = 'direct',
  itemStyle = 'direct-knowledge',
  stimulus = null,
  question,
  correct,
  distractors,
  explanation,
}) {
  return {
    id,
    formId,
    domain,
    type: 'single-choice',
    acsTask,
    acsCode,
    sourceRefs,
    evidenceType,
    itemStyle,
    stimulus,
    choices: [correct, ...distractors],
    correctAnswer: 0,
    question,
    explanation: sentence(explanation),
    premiumEligible: true,
    reviewStatus: 'source-reviewed',
    reviewedAt: REVIEWED_AT,
  }
}

function expandConcepts({ prefix, start, domain, concepts }) {
  const questions = []
  let idNumber = start

  for (let formIndex = 0; formIndex < EXTRA_FORM_IDS.length; formIndex += 1) {
    for (const concept of concepts) {
      const variant = concept.variants[formIndex]
      questions.push(q({
        id: `p107-cert-${prefix}-${pad(idNumber)}`,
        formId: EXTRA_FORM_IDS[formIndex],
        domain,
        acsTask: concept.acsTask,
        acsCode: concept.acsCode,
        sourceRefs: concept.sourceRefs,
        evidenceType: concept.evidenceType,
        itemStyle: concept.itemStyle,
        stimulus: variant.stimulus || concept.stimulus || null,
        question: variant.question,
        correct: variant.correct || concept.correct,
        distractors: variant.distractors || concept.distractors,
        explanation: variant.explanation || concept.explanation,
      }))
      idNumber += 1
    }
  }

  return questions
}

