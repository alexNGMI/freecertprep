const guidance = {
  'hardware-diagnostics': 'Use the observed POST, power, thermal, display, printer, or component evidence to isolate the failing layer before replacing unrelated hardware.',
  'network-connectivity': 'Follow the evidence from physical link through addressing, name resolution, service reachability, wireless conditions, and segmentation.',
  'mobile-peripherals': 'Separate compatibility, cable, accessory, synchronization, permission, and managed-device settings before replacing working mobile hardware.',
  'storage-configuration': 'Match interface, health, performance, capacity, and resilience evidence to the supported storage action.',
  'virtualization-cloud': 'Choose the service model, virtual network, isolation, or recovery feature that directly satisfies the workload requirement.',
  'os-tools': 'Use the native operating-system command, setting, log, or recovery tool that directly addresses the observed state while preserving user data.',
  'security-response': 'Contain active risk first, preserve useful evidence, and restore access with least privilege, strong authentication, encryption, and approved policy.',
  'software-mobile': 'Use scope, recent changes, logs, profiles, services, permissions, and comparison tests to repair the smallest likely software fault.',
  'operational-workflow': 'Apply approval, safety, privacy, backup, documentation, communication, validation, and rollback requirements around the technical work.',
}

function practical({
  id,
  domain,
  objectiveId,
  category,
  question,
  task,
  left,
  right,
  artifact,
  reasons,
}) {
  const evidenceItems = artifact.type === 'table'
    ? artifact.rows.map((row) => ({ label: row[0], value: row.slice(1).join(' | ') }))
    : (artifact.lines || artifact.items).map((value, index) => ({
        label: `${artifact.type === 'console' ? 'Observation' : 'Requirement'} ${index + 1}`,
        value,
      }))
  return {
    id,
    domain,
    objectiveId,
    conceptId: `${id}-practical`,
    type: 'pbq-matching',
    practicalCategory: category,
    question,
    itemsLeft: left,
    itemsRight: right,
    correctMatches: [0, 1, 2],
    explanation: guidance[category],
    pbq: {
      title: artifact.title,
      scenario: `${question} ${task}`,
      evidence: [{ title: artifact.title, items: evidenceItems }],
      category,
      task,
      artifacts: [artifact],
    },
    componentFeedback: left.map((label, index) => ({
      label,
      action: `Select "${right[index]}" for this item.`,
      why: `${reasons[index]} ${guidance[category]}`,
    })),
  }
}

